let str1 = "Hello";
let str2 = "Hi";

console.log("The value of str1: ", str1);
console.log("The value of str2: ", str2);

let concatenation = str1 + str2;

console.log("The value of str1 and str2 concatenated: ", concatenation);

let waffles = "I Love Waffles!";

console.log("The value of variable waffles: ", waffles);

console.log("The number of characters in waffles (or length): ", waffles.length);

console.log("Waffles to uppercase: ", waffles.toUpperCase());

console.log("Waffles to lowercase: ", waffles.toLowerCase());

console.log("Waffles first character is: ", waffles.charAt(0));

console.log("Waffles second character is: ", waffles.charAt(1));

console.log("Waffles last character is: ", waffles.charAt(waffles.length-1));

let k = 2;

console.log("The character at index k(=" + k + ")", waffles.charAt(k));

console.log("Waffles with an 'a' appended: ", waffles + 'a');