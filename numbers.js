let num1 = 7;
let num2 = 2;

console.log("num1 is =", num1);
console.log("num2 is =", num2);
console.log()

console.log("The '+' operator adds two integers, returning their sum");
console.log(num1, " + ", num2, " = ", num1+num2);
console.log()

console.log("The '-' operator subtracts two integers, returning their difference");
console.log(num1, " - ", num2, " = ", num1-num2);
console.log()

console.log("The '*' operator multiplies two integers, returning their product");
console.log(num1, " * ", num2, " = ", num1*num2);
console.log()

console.log("The '/' operator divides two integers, returning their quotient");
console.log(num1, " / ", num2, " = ", num1/num2);
console.log()

console.log("The '%' operator returns a value equal to the integer remainder of the division 'num1/num2'");
console.log(num1, " % ", num2, " = ", num1%num2);
console.log()

console.log("The **' operator is for exponentiation, and raises num1 to the power of num2");
console.log(num1, " ** ", num2, " = ", num1**num2);
console.log()

console.log("The code 'Math.floor(num1 / num2)' returns the integer results of the division 'num1/num2'");
console.log("Math.floor(", num1, " / ", num2, ") = ", Math.floor(num1 / num2));
